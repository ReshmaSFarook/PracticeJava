package org.controlflow;

public class ForLoops {
    public static void main(String[] args) {

        for (int i = 0; i < 5; i++) {
            System.out.println("I love for loops");
        }

        for (int i=1;i<=10;i++){
            System.out.println(i);
        }

        for(int i=10; i>0;i--){
            System.out.println(i);
        }
    }

}
